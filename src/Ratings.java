/**
 * A class for creating and storing Ratings objects
 */
public class Ratings {
    //VARIABLES
    private String Source;
    private String Value;

    //GET FUNCTIONS
    /**
     * Returns the name of the Rating object
     * @return the name of the rating source
     */
    public String getSource() {return Source;}

    /**
     * Returns the value of the Rating object
     * @return the value of the rating (as a string)
     */
    public String getValue() {return Value;}
}
